    private void jButton3UbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3UbahActionPerformed
        // TODO add your handling code here:
        try {
            int id = Integer.parseInt(jTextField1.getText());
            String nama = jTextField2Nama.getText();
            String alamat = jTextField3Alamat.getText();
            String jenispakaian = jTextField4JP.getText();
            int berat = Integer.valueOf(jTextField5Berat.getText());
            int Harga = Integer.valueOf(jTextField6Harga.getText());
            int Total = Integer.valueOf(jTextField7Total.getText());
            
            ml.setId(id);
            ml.setNama(nama);
            ml.setAlamat(alamat);
            ml.setJenispakaian(jenispakaian);
            ml.setBerat(berat);
            ml.setHarga(Harga);
            ml.setTotal(Total);
            
            cl.ubahData(ml);
            
            bersih();
            dataTable();
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(this, "Masukan nama pelanggan");
        }
    }//GEN-LAST:event_jButton3UbahActionPerformed

