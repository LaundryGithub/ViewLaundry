    private void jButton2SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2SimpanActionPerformed
        // TODO add your handling code here:
        ControllerLaundry cl = new ControllerLaundry();
        ModelLaundry ml = new ModelLaundry();
        
        String Nama = jTextField2Nama.getText();
        String Alamat = jTextField3Alamat.getText();
        String JenisPakaian = jTextField4JP.getText();
        Berat = Integer.valueOf(jTextField5Berat.getText());
        harga = Integer.valueOf(jTextField6Harga.getText());
        total = Integer.valueOf(jTextField7Total.getText());
        
        try {
            ml.setNama(Nama);
            ml.setAlamat(Alamat);
            ml.setJenispakaian(JenisPakaian);
            ml.setBerat(Berat);
            ml.setHarga(harga);
            ml.setTotal(total);
            cl.simpanLaundry(ml);
            dataTable();
//            JOptionPokane.showMessageDialog(this, "berhasil disimpan");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "gagal disimpan");
            System.err.println(""+e);
        }
        
    }//GEN-LAST:event_jButton2SimpanActionPerformed

